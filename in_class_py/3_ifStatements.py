x=12.5; y=-11.2
print(" x=", x, " y=", y)
print("\n Example 1 illustrates a simple if without an else")

if x <= y:
    print(" x is less than y")
print("\n Example 2 illustrates a simple if-else")

if x <= y:
    print(" x is less than y")
else:
    print(" x is greater than y")

print("\n Example 3 illustrates if-elif")
if x <= y:
    print(" x is less than y")
elif x == y:
    print(" x equals y")
elif x > y:
    print(" x is greater than y")

